#include "libft.h"

int	ft_isalnum(char *str)
{
	if (!((*str <= 'A' && *str >= 'Z') || (*str <= 'a' && *str >= 'z')))
		return (0);
	else if (!(*str <= '0' && *str >= '9'))
		return (0);
	else
		return (1);
}
